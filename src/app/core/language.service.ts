@Injectable({ providedIn: 'root' })
export class LanguageService {
  readonly current = signal<LanguageCode>('en');

  toggle(): void {
    this.current.set(this.current() === 'en' ? 'fr' : 'en');
  }

  t(key: string): string {
    return TRANSLATIONS[this.current()]?.[key] ?? TRANSLATIONS['en']?.[key] ?? key;
  }
}
